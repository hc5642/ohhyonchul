package com.ohhyonchul.fepmanager.dto;

public class FepLineDto {
	
	private int seqNum;
	private String instCode;
	private String instName;
	private String applCode;
	private String applName;
	private String typeClcd;
	public int getSeqNum() {
		return seqNum;
	}
	public void setSeqNum(int seqNum) {
		this.seqNum = seqNum;
	}
	public String getInstCode() {
		return instCode;
	}
	public void setInstCode(String instCode) {
		this.instCode = instCode;
	}
	public String getInstName() {
		return instName;
	}
	public void setInstName(String instName) {
		this.instName = instName;
	}
	public String getApplCode() {
		return applCode;
	}
	public void setApplCode(String applCode) {
		this.applCode = applCode;
	}
	public String getApplName() {
		return applName;
	}
	public void setApplName(String applName) {
		this.applName = applName;
	}
	public String getTypeClcd() {
		return typeClcd;
	}
	public void setTypeClcd(String typeClcd) {
		this.typeClcd = typeClcd;
	}
	
	

}
